<?php

namespace ThaoHR\Events\Client;

use ThaoHR\Client;

abstract class ClientEvent
{
    /**
     * @var Client
     */
    protected $client;

    public function __construct(Client $client)
    {
        $this->client = $client;
    }

    /**
     * @return Client
     */
    public function getClient()
    {
        return $this->client;
    }
}